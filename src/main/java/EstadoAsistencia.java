/**
 * Enum que representa si invitado esta presente, ausente o atrasado.
 *
 */
public enum EstadoAsistencia {

    /** El invitado asistio puntualmente a la reunion. */
    PRESENTE,

    /** El invitado no asistio a la reunion. */
    AUSENTE,

    /** El invitado asistio pero llego atrasado. */
    RETRASADO
}