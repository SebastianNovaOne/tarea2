/**
 * Representa los tipos posibles de reunion.
 *
 * Cada tipo define el objetivo de la reunion.
 */
public enum TipoReunion {

    /** Reunion de caracter tecnico. */
    TECNICA,

    /** Reunion enfocada en marketing */
    MARKETING,

    /** Reunion que no cabe en los otros 2 tipos. */
    OTRO
}